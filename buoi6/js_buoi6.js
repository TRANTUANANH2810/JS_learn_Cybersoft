// ! Vong lap While

function vongLapWhile(toida) {
    i = 1;
    while (i < toida) {
        document.write("So ::: " + i + "<br>");
        i++;
    }
}

// !Vong lap Do While
function vongLapDoWhile(toida) {
    i = 1;
    do {
        document.write("So ::: " + i + " <Br>");
        i++;
    } while (i < toida);
}
